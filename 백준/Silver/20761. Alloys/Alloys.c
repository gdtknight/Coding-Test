/*
 * x + y + z = 1
 * hardness = x * y
 * price = x + y     -> fixed
 * max (x * y), when (x + y) = c
 * x = c / 2, y = c / 2 -> maximum
 */
#include <stdio.h>

int main(void) {
  double c;

  scanf("%lf", &c);
  if (c > 1)
    c = 1;
  printf("%.6g\n", (c / 2.0) * (c / 2.0));
  return (0);
}
